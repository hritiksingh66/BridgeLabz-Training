public class HarryAge {
  public static void main(String[] args) {
    // given birth year of Harry 
    int brth_yr = 2000;

    // Present year 
    int curr_yr = 2024;
    
    // calculating current age of Harry
    int age = curr_yr - brth_yr;
    
    // Printing age of Harry
    System.out.println("The present age of Harry is "+age);
  }
}
