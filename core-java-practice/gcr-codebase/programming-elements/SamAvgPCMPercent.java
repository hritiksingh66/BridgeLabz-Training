public class SamAvgPCMPercent {

  public static void main(String[] args) {

    // Sam's mathematics marks
    int math_marks = 94;

    // Sam's physics marks
    int phy_marks = 95;

    // Sam's chemistry marks 
    int chem_marks = 96;

    // calculating average marks in PCM of Sam 
    double avg_marks = (math_marks + phy_marks + chem_marks) / 3;

    // Print average percentage of Sam in PCM 
    System.out.println("Average percentage of Sam in PCM is "+avg_marks + " %");
  }
  
}
